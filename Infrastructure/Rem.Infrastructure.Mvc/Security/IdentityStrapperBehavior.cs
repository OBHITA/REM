using System.ServiceModel.Channels;
using System.ServiceModel.Description;
using System.ServiceModel.Dispatcher;
using Microsoft.IdentityModel.Protocols.WSTrust;

namespace Rem.Infrastructure.Mvc.Security
{
    /// <summary>
    /// Class for behaviing identity strapper.
    /// </summary>
    public class IdentityStrapperBehavior : IEndpointBehavior
    {
        #region Public Methods

        /// <summary>
        /// Implement to pass data at runtime to bindings to support custom behavior.
        /// </summary>
        /// <param name="endpoint">The endpoint to modify.</param>
        /// <param name="bindingParameters">The objects that binding elements require to support the behavior.</param>
        public void AddBindingParameters ( ServiceEndpoint endpoint, BindingParameterCollection bindingParameters )
        {
        }

        /// <summary>
        /// Implements a modification or extension of the client across an endpoint.
        /// </summary>
        /// <param name="endpoint">The endpoint that is to be customized.</param>
        /// <param name="clientRuntime">The client runtime to be customized.</param>
        public void ApplyClientBehavior ( ServiceEndpoint endpoint, ClientRuntime clientRuntime )
        {
            clientRuntime.MessageInspectors.Add ( new MessageInspector () );
        }

        /// <summary>
        /// Implements a modification or extension of the service across an endpoint.
        /// </summary>
        /// <param name="endpoint">The endpoint that exposes the contract.</param>
        /// <param name="endpointDispatcher">The endpoint dispatcher to be modified or extended.</param>
        public void ApplyDispatchBehavior ( ServiceEndpoint endpoint, EndpointDispatcher endpointDispatcher )
        {
        }

        /// <summary>
        /// Implement to confirm that the endpoint meets some intended criteria.
        /// </summary>
        /// <param name="endpoint">The endpoint to validate.</param>
        public void Validate ( ServiceEndpoint endpoint )
        {
            var other = endpoint.Behaviors.Find<ClientCredentials> ();

            if ( other == null )
            {
                return;
            }

            endpoint.Behaviors.Remove ( other.GetType () );

            var item = new FederatedClientCredentials ( other );

            endpoint.Behaviors.Add ( item );
        }

        #endregion
    }
}
